// 문자결합연산자 - 여러개의 문자를 하나의 문자형 데이터로 결합할 때 사용
// ex "<br>" 넣으면 스타일로도 적용된다.
var num1 = 15;
var num2 = 2;
var result;
result = num1 + num2;
document.write(result + "<br>"); // plus
result = num1 - num2;
document.write(result + "<br>"); // minus
result = num1 * num2;
document.write(result + "<br>"); // multiply
result = num1 / num2;
document.write(result + "<br>"); // divide
result = num1 % num2;
document.write(result + "<br>"); // Remainder